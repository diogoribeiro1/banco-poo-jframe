    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        try {
            String nome = txtNome.getText();
            GerenteDao dao = new GerenteDao();
            dao.selectForName(nome);
            if (dao.selectForName(nome) == null) {
                dao.cadastrarGerente(nome);
                JOptionPane.showMessageDialog(null, "Gerente cadastrado com Sucesso!");
                FrmPrincipal frmPrin = new FrmPrincipal();
                frmPrin.setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Gerente j√° existe!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmCadastroGerente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

